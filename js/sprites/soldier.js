// Gera templates espelhados para direção esquerda
(function generateMirroredSoldierTemplates() {
    SOLDIER_TEMPLATES.idle_left = mirrorTemplate(SOLDIER_TEMPLATES.idle_right);
    SOLDIER_TEMPLATES.walk1_left = mirrorTemplate(SOLDIER_TEMPLATES.walk1_right);
    SOLDIER_TEMPLATES.walk2_left = mirrorTemplate(SOLDIER_TEMPLATES.walk2_right);
})();

// Obtém template correto baseado em direção e estado
function getSoldierTemplate(direction, isMoving) {
    const dirNames = ['up', 'right', 'down', 'left'];
    const dirName = dirNames[direction] || 'down';
    
    if (!isMoving) {
        return SOLDIER_TEMPLATES[`idle_${dirName}`] || SOLDIER_TEMPLATES.idle_down;
    }
    
    // Ciclo de animação: idle -> walk1 -> idle -> walk2
    const walkFrame = Math.floor(frameCount / 6) % 4;
    if (walkFrame === 0 || walkFrame === 2) {
        return SOLDIER_TEMPLATES[`idle_${dirName}`] || SOLDIER_TEMPLATES.idle_down;
    } else if (walkFrame === 1) {
        return SOLDIER_TEMPLATES[`walk1_${dirName}`] || SOLDIER_TEMPLATES.walk1_down;
    } else {
        return SOLDIER_TEMPLATES[`walk2_${dirName}`] || SOLDIER_TEMPLATES.walk2_down;
    }
}

